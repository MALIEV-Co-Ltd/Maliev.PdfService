using Asp.Versioning;
using Maliev.Aspire.ServiceDefaults.Authorization;
using Maliev.PdfService.Api.Authorization;
using Maliev.PdfService.Api.Models.Data;
using Maliev.PdfService.Api.Services;
using Maliev.PdfService.Domain.Entities;
using Microsoft.AspNetCore.Mvc;

namespace Maliev.PdfService.Api.Controllers;

/// <summary>
/// Controller for rendering customer-facing material datasheet PDFs.
/// </summary>
[ApiVersion("1")]
[Route("pdf/v{version:apiVersion}/material-datasheets")]
[ApiController]
public sealed class MaterialDatasheetsController(IPdfGenerator pdfGenerator) : ControllerBase
{
    /// <summary>
    /// Renders a material datasheet PDF and returns the generated bytes directly.
    /// </summary>
    /// <param name="request">The material datasheet data.</param>
    /// <returns>The rendered PDF file.</returns>
    [HttpPost("render")]
    [RequirePermission(PdfPermissions.GenerationCreate)]
    [Produces("application/pdf")]
    [ProducesResponseType(typeof(FileContentResult), StatusCodes.Status200OK)]
    public async Task<IActionResult> Render([FromBody] MaterialDatasheetData request)
    {
        var pdfBytes = await pdfGenerator.GeneratePdfAsync(DocumentType.MaterialDatasheet, request);
        return File(pdfBytes, "application/pdf", $"{SanitizeFileName(request.Slug)}-maliev-material-datasheet.pdf");
    }

    private static string SanitizeFileName(string value)
    {
        var invalidCharacters = Path.GetInvalidFileNameChars();
        var safe = new string(value.Select(character => invalidCharacters.Contains(character) ? '-' : character).ToArray());
        return string.IsNullOrWhiteSpace(safe) ? "maliev-material-datasheet" : safe;
    }
}
